CREATE 
    TABLE VBR_IFRS.CHAT 
    ( 
        ID BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY, 
        NAME VARCHAR(100) NOT NULL, 
        MESSAGE VARCHAR(3000), 
        SEND_TIME TIMESTAMP, 
        PRIMARY KEY (ID) 
    )
    
    
CREATE 
    TABLE VBR_IFRS.CHAT_USERS 
    ( 
        ID DECIMAL(5,0) NOT NULL GENERATED BY DEFAULT AS IDENTITY, 
        IP VARCHAR(255), 
        NAME VARCHAR(255), 
        STATUS VARCHAR(10), 
        COLOR VARCHAR(255) 
    )